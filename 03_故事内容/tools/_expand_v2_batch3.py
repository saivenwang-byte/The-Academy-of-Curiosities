def count(path):
    raw = path.read_text(encoding="utf-8")
    for pat in [r"^序\s*[·・]", r"^[一二三四五六]、"]:
        m = re.search(pat, raw, re.M)
        if m:
            raw = raw[m.start():]
            break
    for marker in ["【语感编辑", "---\n【", "（案"]:
        idx = raw.find(marker)
        if idx > 0:
            raw = raw[:idx]
    raw = re.sub(r"\*\*", "", raw)
    return len(re.findall(r"[\u4e00-\u9fff]", raw))

