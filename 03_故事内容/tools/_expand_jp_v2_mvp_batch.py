def strip_cn(raw: str) -> str:
    cut = re.search(r"\n---\n\n【读者可以试", raw)
    if cut:
        raw = raw[: cut.start()]
    lines = []
    for line in raw.splitlines():
        s = line.strip()
        if s.startswith("《学堂") or s.startswith("第1卷") or s.startswith("单元"):
            continue
        if re.match(r"^=+$", s) or (s.startswith("---") and len(s) < 5):
            continue
        lines.append(line)
    return "\n".join(lines).strip()


